//doubly linear
#include<iostream>
using namespace std;

struct node
{
    int data;
    struct node *next;
    struct node *prev;
};

typedef struct node NODE;
typedef struct node* PNODE;


class DoublyLL
{
   private:
        PNODE first;
        
        int iCount;
    public:
        DoublyLL()
        {
            cout<<"object of DoublyLL gets created:\n";
            this->first=NULL;
            
            this->iCount=0;
        }

        void InsertFirst(int no)
        {}
        void InsertLast(int no)
        {}
        void InsertAtPos(int no,int pos)
        {}
        void DeleteFirst()
        {}
        void DeleteLast()
        {}
        void DeleteAtPos(int pos)
        {}
        void Display()
        {}
        int count()
        {
            return iCount;
        }
};

int main()
{
    DoublyLL obj;

    int iRet=0;

    obj.InsertFirst(51);
    obj.InsertFirst(21);
    obj.InsertFirst(11);

    obj.Display();
    iRet=obj.count();
    
    cout<<"number of nodes are:"<<iRet<<"\n";

    obj.InsertLast(101);
    obj.InsertLast(111);
    obj.InsertLast(121);
    obj.Display();
    iRet=obj.count();
    
    cout<<"number of nodes are:"<<iRet<<"\n";

    obj.DeleteFirst();
    obj.Display();
    iRet=obj.count();
    
    cout<<"number of nodes are:"<<iRet<<"\n";

    obj.DeleteLast();
    obj.Display();
    iRet=obj.count();
    
    cout<<"number of nodes are:"<<iRet<<"\n";
    
    obj.InsertAtPos(105,3);
    obj.Display();
    iRet=obj.count();

    
    
    cout<<"number of nodes are:"<<iRet<<"\n";

    obj.DeleteAtPos(4);
    obj.Display();
    iRet=obj.count();
    
    cout<<"number of nodes are:"<<iRet<<"\n";

    return 0;


}
